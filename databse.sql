--------------------------------------------------------
set define off;

  CREATE OR REPLACE EDITIONABLE PROCEDURE "BANKING_APP"."CREATE_USER" (
    u_name varchar2,
    u_surname varchar2,
    u_email varchar2,
    u_password varchar2
)
is
    duplicate_email EXCEPTION;
    PRAGMA EXCEPTION_INIT(duplicate_email, -1);
begin
    INSERT INTO users (name, surname, email, password) VALUES (u_name, u_surname, u_email, u_password);

    COMMIT;
EXCEPTION
    WHEN duplicate_email THEN
        RAISE_APPLICATION_ERROR(-20001, 'Email already exists');
    WHEN OTHERS THEN
      RAISE;
end;

/
