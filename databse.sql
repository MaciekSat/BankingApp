set define off;

  CREATE OR REPLACE EDITIONABLE PROCEDURE "BANKING_APP"."GET_USER" (
    u_email in varchar2,
    u_name out varchar2,
    u_surname out varchar2,
    u_hash out varchar2
)
is
begin
    SELECT name, surname, password INTO u_name, u_surname, u_hash FROM users WHERE email=u_email;
EXCEPTION
    WHEN NO_DATA_FOUND THEN
        RAISE_APPLICATION_ERROR(-20002, 'There is no such user');
    WHEN OTHERS THEN
      RAISE;
end;

/
